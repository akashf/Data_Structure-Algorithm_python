    # Queue Data Structure
    #     Implemented using a linked list
    #     Follows First-In-First-Out (FIFO) approach
    
    # Basic operations:
    #     Enqueue: add element to the end (tail)
    #     Dequeue: remove element from the front (head)
    
    # Types of Queues
    #     Queue: basic FIFO queue
    #     Priority Queue:
    #         Elements have a priority value Dequeue operation checks priority 
    #         and removes the highest-priority element first If priorities are the same,
    #         the older element is removed first
    #     Deque (Double-Ended Queue):
    #         Elements can be added or removed from both ends (head and tail)